package homework3;

public class Sum_4 {
    public static void main(String[] args) {
        //4. Write a Java program to find the sum of first n natural numbers using for loop.
        int sum = 0;
        for (int j = 1; j <= 4; j++) {
            //sum = sum + j;
            sum += j;
            System.out.print("Sum 4=+: " + sum + " ");
        }
    }
}
